def build_graph_context(person: dict) -> str:
    """person 딕셔너리(역할/성향/구절/성경서)를 제자 프롬프트의 [Graph DB 정보] 한 줄로 요약."""
    parts = []
    if person.get("role"):
        parts.append(f"역할: {person['role']}")
    if person.get("traits"):
        parts.append(f"성향: {person['traits']}")
    if person.get("speech_style"):
        parts.append(f"말투: {person['speech_style']}")
    if person.get("quote"):
        ref = f" ({person['quote_ref']})" if person.get("quote_ref") else ""
        parts.append(f"상징 구절: \"{person['quote']}\"{ref}")
    display_books = person.get("book_full_names") or person.get("books")
    if display_books:
        parts.append(f"연관 성경서: {', '.join(display_books)}")
    return " · ".join(parts) or "관련 정보 없음"


def title_for(gender: str) -> str:
    """회원가입 시 받은 성별 → 호칭. adam=아담의 후손, eve=하와의 후손."""
    return "자매님" if (gender or "").lower() in ("eve", "female", "f", "여", "여자") else "형제님"


def build_prompt(
    person: dict,
    user_mbti: str,
    message: str,
    history: str = "",
    context: str = "",
    user_name: str = "",
    shared_memory: str = "",
    gender: str = "adam",
) -> str:
    """person_id에 따라 예수님/제자 프롬프트를 조립한다."""
    person_id = person.get("id", "")

    if person_id == "jesus":
        return JESUS_SYSTEM_PROMPT.format(
            user_name=user_name or "친구",
            user_mbti=user_mbti,
            shared_memory=shared_memory or "(아직 없음)",
            context=context or "관련 구절 없음",
            history=history or "(첫 대화)",
        )

    # 예전엔 제자 프롬프트에 context(검색된 성경 구절)를 아예 안 넘겨서,
    # 제자들이 벡터 검색 결과를 못 보고 답했습니다. 이제 함께 전달합니다.
    common = DISCIPLE_SYSTEM_PROMPT_COMMON.format(
        disciple_name=person.get("name", person_id),
        user_name=user_name or "친구",
        user_title=title_for(gender),
        user_mbti=user_mbti,
        user_question=message,
        shared_memory=shared_memory or "(아직 없음)",
        graph_context=build_graph_context(person),
        context=context or "관련 구절 없음",
        history=history or "(첫 대화)",
    )
    persona = DISCIPLE_PERSONAS.get(person_id, "")
    return f"{common}\n{persona}\n\n{person.get('name', person_id)}의 답변:"
