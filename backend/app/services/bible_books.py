def to_abbr(name: str) -> str | None:
    """책 이름(전체/약어/별칭) → 스냅샷이 쓰는 약어. 모르면 None."""
    if not name:
        return None
    n = name.strip()
    if n in ABBR_TO_FULL:          # 이미 약어
        return n
    if n in FULL_TO_ABBR:          # 전체 이름
        return FULL_TO_ABBR[n]
    if n in _ALIASES:              # 별칭
        return _ALIASES[n]
    n2 = n.replace(" ", "")
    return FULL_TO_ABBR.get(n2) or _ALIASES.get(n2)


def to_full(name: str) -> str:
    """약어 → 전체 이름. 모르면 입력 그대로 (화면에 뭐라도 나오게)."""
    if not name:
        return ""
    return ABBR_TO_FULL.get(name.strip(), name.strip())


def normalize_books(books) -> list[str]:
    """
    graph/목업이 준 성경서 목록을 벡터 필터용 약어 리스트로 변환.
    모르는 이름은 조용히 버립니다 — 잘못된 값 하나 때문에 필터가 0건이 되어
    구절이 통째로 사라지는 것보다, 아는 책으로만 좁히는 편이 안전합니다.
    """
    if not books:
        return []
    out: list[str] = []
    for b in books:
        a = to_abbr(b)
        if a and a not in out:
            out.append(a)
    return out
