# 약어 → 전체 이름 (화면 표기를 전체 이름으로 되돌릴 때)
ABBR_TO_FULL: dict[str, str] = {v: k for k, v in FULL_TO_ABBR.items()}

# 표기 흔들림 흡수용 별칭 (팀·자료마다 다르게 쓰는 이름들)
